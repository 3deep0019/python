# Range data type represents a sequence of numbers.

#  form 1 - range(10)
r = range(10)
for i in r : print(i) 
# --- from 2 - range(10,20)

r = range(10,20)
for i in r : print(i)


#  from 3 - range(10,20,2)

r = range(10,20,2)
for i in r : print(i)

# we can access element present in the range data type by using index

r = range(10,20)
print(r[0])


