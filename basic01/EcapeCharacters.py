# In string literals we can use escape characters to associates a special meaning .

# 1) \n ->  New Line
# 2) \t ->  Horizontal Tab
# 3) \r ->  Carriage Return
# 4) \b ->  Back Space
# 5) \f ->  Form Feed
# 6) \v ->  Vertical Tab
# 7) \' ->  Single Quote
# 8) \" ->  Double Quote
# 9) \\ ->  Back Slash Symbol
s = "manish\npathak"
print(s)

s= "manish\tpathak"
print(s)

s = 'this is \" symbole'
print(s)



