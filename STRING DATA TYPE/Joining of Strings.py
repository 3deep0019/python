#    ---------------> We can join a Group of Strings (List OR Tuple) wrt the given Seperator.
#    ---------------> s = seperator.join(group of strings)

# Eg 1:

t = ('sunny', 'bunny', 'chinny')
s = '-'.join(t)
print(s)


l = ['hyderabad', 'singapore', 'london', 'dubai']
s = ':'.join(l)
print(s)