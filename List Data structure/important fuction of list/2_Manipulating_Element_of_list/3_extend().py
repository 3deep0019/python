# 3) extend() Function:
# ------>   To add all items of one list to another list
 #   l1.extend(l2)
 #   all items present in l2 will be added to l1


order1=["Chicken","Mutton","Fish"] 
order2=["RC","KF","FO"] 
order1.extend(order2) 
print(order1) 
